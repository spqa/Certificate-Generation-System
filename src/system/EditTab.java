    public EditTab(final JTabbedPane tabPane,int StuID) {
        this.StuId=StuID;
        initComponents();
        this.pane=tabPane;
        try {
            MaskFormatter mf=new MaskFormatter("####-##-##");
            mf.install(txtStudentDOB);
            
        } catch (ParseException ex) {
            Logger.getLogger(EditTab.class.getName()).log(Level.SEVERE, null, ex);
        }
        Student st=Student.getStudentById(StuID);
        txtStudentName.setText(st.getFullname());
        txtStudentDOB.setText(st.getDOB());
        if (st.getGender().equals("Male")) {
            rdMale.setSelected(true);
        }else if(st.getGender().equals("Female")){
        rdFemale.setSelected(true);
        }
        
        if (st.getFeeID()==1) {
            rdAll.setSelected(true);
        }else if(st.getFeeID()==2){
        rdInstallment.setSelected(true);
        }
        
        DefaultComboBoxModel<Course> CourseModel=new DefaultComboBoxModel<>(Course.getAllCourses());
        cbCourseName.setModel(CourseModel);
    }

