    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            PreparedStatement pre=DBConnect.connectDatabase().prepareStatement("Update Student set Fullname=?,DOB=?,Gender=?,CourseId=?,FeeId=? where StuId=?");
            pre.setString(1, txtStudentName.getText());
            DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
            Date utilDate=df.parse(txtStudentDOB.getText().trim());
            java.sql.Date sqldate=new java.sql.Date(utilDate.getTime());
            pre.setDate(2, sqldate);
            if (rdFemale.isSelected()) {
                pre.setString(3, rdFemale.getText());
            }else if(rdMale.isSelected()){
            pre.setString(3, rdMale.getText());
            }
            
            if (rdAll.isSelected()) {
                pre.setInt(5, 1);
            }else if(rdInstallment.isSelected()){
            pre.setInt(5, 2);
            }
            
            pre.setInt(4, ((Course)cbCourseName.getSelectedItem()).getId());
            pre.setInt(6, StuId);
            boolean rs=pre.execute();
            if (rs==false) {
                JOptionPane.showMessageDialog(null, "Update Successfully", "Information", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/res/ok40.png")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditTab.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "The format of date should be YYYY-MM-DD/n Please check again!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


